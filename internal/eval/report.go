// WriteSnapshot writes the run result as a timestamped JSON snapshot under
// outDir/snapshots/ and refreshes snapshots/baseline.json so trends are
// git-diffable. It returns the timestamped snapshot path.
func WriteSnapshot(outDir string, r *RunResult) (string, error) {
	snapDir := filepath.Join(outDir, "snapshots")
	if err := os.MkdirAll(snapDir, 0o750); err != nil {
		return "", err
	}
	data, err := json.MarshalIndent(r, "", "  ")
	if err != nil {
		return "", err
	}
	data = append(data, '\n')
	stamp := r.GeneratedAt.UTC().Format("20060102T150405Z")
	tsPath := filepath.Join(snapDir, "run-"+stamp+".json")
	if err := os.WriteFile(tsPath, data, 0o600); err != nil {
		return "", err
	}
	if err := os.WriteFile(filepath.Join(snapDir, "baseline.json"), data, 0o600); err != nil {
		return "", err
	}
	return tsPath, nil
}

// WriteScoreboard regenerates the public benchmark document at outDir/BENCHMARKS.md
// from the run result, so its prose can never drift from the numbers.
func WriteScoreboard(outDir string, r *RunResult) error {
	if err := os.MkdirAll(outDir, 0o750); err != nil {
		return err
	}
	return os.WriteFile(filepath.Join(outDir, "BENCHMARKS.md"), []byte(Scoreboard(r)), 0o600)
}

// LoadSnapshot reads the latest committed benchmark snapshot from
// outDir/snapshots/baseline.json for `ozy eval report`.
func LoadSnapshot(outDir string) (*RunResult, error) {
	path := filepath.Join(outDir, "snapshots", "baseline.json")
	// #nosec G304 -- reads the committed eval snapshot at a caller-provided out dir.
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("read %s: %w", path, err)
	}
	var r RunResult
	if err := json.Unmarshal(data, &r); err != nil {
		return nil, fmt.Errorf("parse %s: %w", path, err)
	}
	return &r, nil
}

// Scoreboard renders the human-readable public benchmark Markdown from a run
// result. It summarizes each family at a glance; the metric mathematics live in
// METHODOLOGY.md, which the scoreboard links to rather than inlining.
func Scoreboard(r *RunResult) string {
	var b strings.Builder
	fmt.Fprintln(&b, "# Ozy Benchmarks")
	fmt.Fprintln(&b)
	verdict := "PASS ✅"
	if r.Failed() {
		verdict = "FAIL ❌"
	}
	fmt.Fprintf(&b, "**Verdict: %s**\n\n", verdict)
	fmt.Fprintf(&b, "_Generated %s · commit `%s` · corpus v%d · model `%s` · semantic leg %s_\n\n",
		r.GeneratedAt.UTC().Format(time.RFC3339),
		shortCommit(r.Provenance.GitCommit),
		r.Provenance.CorpusVersion,
		r.Provenance.Model,
		ranWord(r.Provenance.SemanticRan),
	)
	fmt.Fprintln(&b, "> Public scoreboard. Metric definitions, calibration, and gold-set hygiene rules live in [METHODOLOGY.md](METHODOLOGY.md).")
	fmt.Fprintln(&b)

	if r.Discovery != nil {
		writeDiscoverySection(&b, r.Discovery)
	}
	if r.Invocation != nil {
		writeInvocationSection(&b, r.Invocation)
	}
	if r.Ergonomics != nil {
		writeErgonomicsSection(&b, r.Ergonomics)
	}
	if r.TokenEconomy != nil {
		writeTokenSection(&b, r.TokenEconomy)
	}
	if r.Performance != nil {
		writePerformanceSection(&b, r.Performance)
	}
	writeGatesSection(&b, r.Gates)

	if len(r.Hygiene) > 0 {
		fmt.Fprintln(&b, "## Gold-set hygiene warnings")
		fmt.Fprintln(&b)
		for _, h := range r.Hygiene {
			fmt.Fprintf(&b, "- `%s` (%s): %s\n", h.Intent, h.Category, h.Issue)
		}
		fmt.Fprintln(&b)
	}

	fmt.Fprintln(&b, "---")
	fmt.Fprintln(&b, "_Generated by `ozy eval`. Do not edit by hand — regenerate with `ozy eval run`._")
	return b.String()
}

func writeDiscoverySection(b *strings.Builder, d *DiscoveryReport) {
	fmt.Fprintln(b, "## Discovery")
	fmt.Fprintln(b)
	fmt.Fprintln(b, "| Scope | N | Top-1 | Top-3 | MRR | Wrong-server | No-match correct |")
	fmt.Fprintln(b, "| --- | ---: | ---: | ---: | ---: | ---: | ---: |")
	writeDiscoveryRow(b, "overall", d.Overall)
	cats := make([]string, 0, len(d.ByCategory))
	for c := range d.ByCategory {
		cats = append(cats, c)
	}
	sort.Strings(cats)
	for _, c := range cats {
		writeDiscoveryRow(b, c, d.ByCategory[c])
	}
	fmt.Fprintln(b)
}

func writeDiscoveryRow(b *strings.Builder, scope string, m DiscoveryMetrics) {
	fmt.Fprintf(b, "| %s | %d | %s | %s | %s | %s | %s |\n",
		scope, m.N, pct(m.Top1), pct(m.Top3), num(m.MRR),
		dashPct(m.WrongServerRate, scope == CategoryWrongServer),
		dashPct(m.NoMatchCorrectness, scope == CategoryNoMatch || scope == "overall"),
	)
}

func writeInvocationSection(b *strings.Builder, r *InvocationReport) {
	m := r.Overall
	fmt.Fprintln(b, "## Invocation & repair")
	fmt.Fprintln(b)
	fmt.Fprintln(b, "| N | Valid-arg | First-call | Repair | Schema-drift caught | Offline handled | Error clarity |")
	fmt.Fprintln(b, "| ---: | ---: | ---: | ---: | ---: | ---: | ---: |")
	fmt.Fprintf(b, "| %d | %s | %s | %s | %s | %s | %s |\n",
		m.N, pct(m.ValidArgumentRate), pct(m.FirstCallSuccess), pct(m.RepairSuccess),
		pct(m.SchemaErrorRate), pct(m.OfflineHandledRate), pct(m.ErrorClarity))
	fmt.Fprintln(b)
}

func writeErgonomicsSection(b *strings.Builder, r *ErgonomicsReport) {
	m := r.Overall
	fmt.Fprintln(b, "## Agent ergonomics & CLI↔MCP parity")
	fmt.Fprintln(b)
	fmt.Fprintln(b, "| N | Decision | Instruction | Error disposition | Within budget | Parity |")
	fmt.Fprintln(b, "| ---: | ---: | ---: | ---: | ---: | ---: |")
	fmt.Fprintf(b, "| %d | %s | %s | %s | %s | %s |\n",
		m.N, pct(m.DecisionRate), pct(m.InstructionRate), pct(m.ErrorDispositionRate),
		pct(m.WithinBudgetRate), pct(m.ParityRate))
	fmt.Fprintln(b)
	if len(r.NonInstructional) > 0 {
		fmt.Fprintf(b, "_Non-instructional responses: %s_\n\n", strings.Join(r.NonInstructional, ", "))
	}
	if len(r.ParityMismatches) > 0 {
		fmt.Fprintf(b, "_Parity mismatches: %s_\n\n", strings.Join(r.ParityMismatches, ", "))
	}
}

func writeTokenSection(b *strings.Builder, m *TokenEconomyMetrics) {
	fmt.Fprintln(b, "## Token economy")
	fmt.Fprintln(b)
	fmt.Fprintf(b, "Direct-MCP loads **%d** tokens of tool schemas at startup; Ozy loads **%d** (its three tools) — a **%.0f%%** reduction. _Estimator: %s._\n\n",
		m.DirectStartupTokens, m.OzyStartupTokens, m.StartupReductionRatio*100, m.Estimator)
	fmt.Fprintln(b, "| Path | Startup tokens | Tokens-to-success | Largest payload | Broker calls |")
	fmt.Fprintln(b, "| --- | ---: | ---: | ---: | ---: |")
	fmt.Fprintf(b, "| Direct-MCP | %d | %d | — | 1 |\n", m.DirectStartupTokens, m.DirectTokensToSuccess)
	fmt.Fprintf(b, "| Ozy | %d | %d | %d | %d |\n", m.OzyStartupTokens, m.OzyTokensToSuccess, m.LargestPayloadTokens, m.BrokerCalls)
	fmt.Fprintln(b)
}

func writePerformanceSection(b *strings.Builder, r *LatencyReport) {
	fmt.Fprintln(b, "## Performance (latency)")
	fmt.Fprintln(b)
	fmt.Fprintln(b, "| Path | p50 (µs) | p95 (µs) | ops/sec |")
	fmt.Fprintln(b, "| --- | ---: | ---: | ---: |")
	writeLatencyRow(b, "lexical", r.Lexical)
	writeLatencyRow(b, "fusion", r.Fusion)
	writeLatencyRow(b, "end-to-end", r.EndToEnd)
	writeLatencyRow(b, "semantic", r.Semantic)
	fmt.Fprintln(b)
	fmt.Fprintln(b, "_Latency is environment-dependent and reported, not gated; see the run host in the provenance line._")
	fmt.Fprintln(b)
}

func writeLatencyRow(b *strings.Builder, name string, s *LatencyStats) {
	if s == nil {
		return
	}
	fmt.Fprintf(b, "| %s | %.2f | %.2f | %.0f |\n", name, s.P50Micros, s.P95Micros, s.OpsPerSec)
}

func writeGatesSection(b *strings.Builder, gates []GateResult) {
	if len(gates) == 0 {
		return
	}
	fmt.Fprintln(b, "## Gates")
	fmt.Fprintln(b)
	fmt.Fprintln(b, "| Gate | Bound | Threshold | Actual | Result |")
	fmt.Fprintln(b, "| --- | --- | ---: | ---: | --- |")
	for _, g := range gates {
		result := "✅ pass"
		switch {
		case g.Skipped:
			result = "— skipped (" + g.SkipReason + ")"
		case !g.Pass:
			result = "❌ fail"
		}
		fmt.Fprintf(b, "| `%s` | %s | %s | %s | %s |\n",
			g.Name, g.Comparator, num(g.Threshold), num(g.Actual), result)
	}
	fmt.Fprintln(b)
}

func pct(v float64) string { return fmt.Sprintf("%.1f%%", v*100) }

func num(v float64) string { return fmt.Sprintf("%.3f", v) }

// dashPct shows a percentage when the metric applies to the scope, else a dash.
func dashPct(v float64, applies bool) string {
	if !applies {
		return "—"
	}
	return pct(v)
}

func shortCommit(c string) string {
	if len(c) >= 12 {
		return c[:12]
	}
	if c == "" {
		return "unknown"
	}
	return c
}

func ranWord(b bool) string {
	if b {
		return "ran"
	}
	return "skipped"
}
